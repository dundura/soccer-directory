import { neon } from "@neondatabase/serverless";

const sql = neon(process.env.DATABASE_URL);

await sql`
CREATE TABLE IF NOT EXISTS special_events (
  id TEXT PRIMARY KEY DEFAULT gen_random_uuid()::text,
  slug TEXT UNIQUE NOT NULL,
  name TEXT NOT NULL,
  organizer_name TEXT NOT NULL,
  club_name TEXT,
  city TEXT NOT NULL,
  country TEXT DEFAULT 'United States',
  state TEXT NOT NULL,
  event_type TEXT NOT NULL,
  age_group TEXT NOT NULL,
  gender TEXT NOT NULL,
  dates TEXT NOT NULL,
  time TEXT,
  location TEXT,
  cost TEXT,
  description TEXT,
  registration_url TEXT,
  website TEXT,
  email TEXT,
  phone TEXT,
  is_past BOOLEAN DEFAULT false,
  social_media JSONB DEFAULT '{}',
  logo TEXT,
  image_url TEXT,
  team_photo TEXT,
  image_position INTEGER DEFAULT 50,
  hero_image_position INTEGER DEFAULT 50,
  photos JSONB DEFAULT '[]',
  video_url TEXT,
  tagline TEXT,
  announcement_heading TEXT,
  announcement_text TEXT,
  announcement_image TEXT,
  announcement_cta TEXT,
  announcement_cta_url TEXT,
  announcement_heading_2 TEXT,
  announcement_text_2 TEXT,
  announcement_image_2 TEXT,
  announcement_cta_2 TEXT,
  announcement_cta_url_2 TEXT,
  announcement_heading_3 TEXT,
  announcement_text_3 TEXT,
  announcement_image_3 TEXT,
  announcement_cta_3 TEXT,
  announcement_cta_url_3 TEXT,
  featured BOOLEAN DEFAULT false,
  user_id TEXT,
  status TEXT DEFAULT 'pending',
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
)`;

console.log("special_events table created");
